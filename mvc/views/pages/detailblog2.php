            <div class="article-pages">
              <p>Là một trong những đôi giày chạy bộ tốt nhất vào những năm 1994, 1995, Saucony Aya Runner vừa có màn trở lại vô cùng ấn tượng</p>
              <p>Có vẻ như 2019 là khoảng thời gian mà những sản phẩm chạy bộ gặt hái được nhiều thành công nhất từ trước đến nay. Điều này có được là nhờ phong trào chạy bộ đang dần trở lại và phát triển mạnh mẽ hơn. Cũng vì thế mà các thiết kế giày chạy bộ cổ điển cũng ngày càng được chú ý hơn. Chính vì thế, Saucony, một trong những hãng giày chạy bộ tốt nhất hiện nay vừa giới thiệu đến các fan của mình Aya Runner – một huyền thoại được ra mắt vào năm 1994.</p>
              <p>Đôi giày xuất hiện với vẻ ngoài bắt mắt, nổi bật cùng tông màu cam, tím năng động và trẻ trung. Điều này khiến sản phẩm phù hợp hơn cho mùa hè sắp tới. Vì là một đôi giày retro, nên chất liệu của Aya Runner vô cùng đơn giản. Ta có thân giày được cấu tạo từ lưới, da tổng hợp và da lộn, bộ đệm bằng TPU khá dày và outsole cơ bản. Form giày của đôi Aya Runner khá quen thuộc, có nét tương đồng với các mẫu retro runner nổi tiếng như Asics Gel-Lyte III vf Gel-Lyte V.</p>
              <p>Saucony Aya Runner phối màu tím cam sẽ chính thức được lên kệ vào ngày 8 tháng 6 tới với mức giá nhẹ nhàng: $80 USD (khoảng 2 triệu đồng) và được phát hành tại website của Saucony cũng như một số cửa hàng bán lẻ chọn lọc như SNS.</p>
